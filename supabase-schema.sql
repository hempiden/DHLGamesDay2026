-- ========================================================
-- 1. Create Events Table
-- ========================================================
CREATE TABLE IF NOT EXISTS public.events (
    id TEXT PRIMARY KEY,
    name TEXT NOT NULL,
    khmer_name TEXT,
    date TEXT,
    description TEXT,
    sports JSONB NOT NULL DEFAULT '[]'::jsonb,
    theme_color TEXT DEFAULT 'dhl',
    created_by TEXT NOT NULL,
    show_public_teams BOOLEAN DEFAULT false,
    is_enrolment_enabled BOOLEAN DEFAULT true,
    organization_slug TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc', NOW()) NOT NULL
);

-- Index for organization and event fetching lookup
CREATE INDEX IF NOT EXISTS idx_events_organization_slug ON public.events(organization_slug);

-- ========================================================
-- 2. Create Matches Table (using text for dynamic sport types)
-- ========================================================
CREATE TABLE IF NOT EXISTS public.matches (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    event_id TEXT REFERENCES public.events(id) ON DELETE CASCADE,
    created_by TEXT,
    sport_name TEXT NOT NULL,
    match_label TEXT DEFAULT 'Regular Match',
    team_a TEXT NOT NULL,
    team_b TEXT NOT NULL,
    score_a INTEGER DEFAULT 0 NOT NULL,
    score_b INTEGER DEFAULT 0 NOT NULL,
    status TEXT CHECK (status IN ('Upcoming', 'Live', 'Finished')) DEFAULT 'Upcoming' NOT NULL,
    scheduled_date TEXT,
    scheduled_time TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc', NOW()) NOT NULL,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc', NOW()) NOT NULL
);

-- Performance indices
CREATE INDEX IF NOT EXISTS idx_matches_event_id ON public.matches(event_id);
CREATE INDEX IF NOT EXISTS idx_matches_sport_name ON public.matches(sport_name);
CREATE INDEX IF NOT EXISTS idx_matches_status ON public.matches(status);

-- Automated updated_at trigger function
CREATE OR REPLACE FUNCTION update_updated_at_column()
RETURNS TRIGGER AS $$
BEGIN
    NEW.updated_at = TIMEZONE('utc', NOW());
    RETURN NEW;
END;
$$ language 'plpgsql';

DROP TRIGGER IF EXISTS update_matches_updated_at ON public.matches;
CREATE TRIGGER update_matches_updated_at
    BEFORE UPDATE ON public.matches
    FOR EACH ROW
    EXECUTE PROCEDURE update_updated_at_column();

-- ========================================================
-- 3. Create Participants/Athletes/Teams Table
-- ========================================================
CREATE TABLE IF NOT EXISTS public.participants (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    event_id TEXT REFERENCES public.events(id) ON DELETE CASCADE,
    created_by TEXT,
    name TEXT NOT NULL,
    sport_type TEXT NOT NULL,
    is_team BOOLEAN DEFAULT FALSE NOT NULL,
    team_id TEXT, -- Store raw text or bigints. To allow simple local references, we use TEXT.
    photo_url TEXT,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc', NOW()) NOT NULL,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc', NOW()) NOT NULL
);

-- Indexes for speedy roster grouping and category queries
CREATE INDEX IF NOT EXISTS idx_participants_event_id ON public.participants(event_id);
CREATE INDEX IF NOT EXISTS idx_participants_sport_type ON public.participants(sport_type);
CREATE INDEX IF NOT EXISTS idx_participants_is_team ON public.participants(is_team);

DROP TRIGGER IF EXISTS update_participants_updated_at ON public.participants;
CREATE TRIGGER update_participants_updated_at
    BEFORE UPDATE ON public.participants
    FOR EACH ROW
    EXECUTE PROCEDURE update_updated_at_column();

-- ========================================================
-- 4. Create Admin Users Table
-- ========================================================
CREATE TABLE IF NOT EXISTS public.admin_users (
    id TEXT PRIMARY KEY,
    username TEXT NOT NULL UNIQUE,
    email TEXT NOT NULL,
    name TEXT NOT NULL,
    role TEXT CHECK (role IN ('admin', 'super_admin')) NOT NULL,
    status TEXT CHECK (status IN ('pending', 'approved', 'rejected')) NOT NULL,
    password_plain TEXT NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc', NOW()) NOT NULL
);

-- ========================================================
-- 5. Create Event Settings Table
-- ========================================================
CREATE TABLE IF NOT EXISTS public.event_settings (
    key TEXT PRIMARY KEY,
    value TEXT NOT NULL,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc', NOW()) NOT NULL
);

-- ========================================================
-- 6. Create Organization Settings Table
-- ========================================================
CREATE TABLE IF NOT EXISTS public.organization_settings (
