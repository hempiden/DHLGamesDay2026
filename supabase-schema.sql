    contact_email TEXT,
    contact_phone TEXT,
    website TEXT,
    address TEXT,
    footer_motto TEXT,
    updated_at TIMESTAMP WITH TIME ZONE DEFAULT TIMEZONE('utc', NOW()) NOT NULL
);

